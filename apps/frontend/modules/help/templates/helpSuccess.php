<h2>Grabeeter User Guide</h2>

<h3>Dear Grabeeter-User</h3>

<p>Grabeeter is an application that allows you to search tweets of Twitter users registered on Grabeeter and to save the results locally.
To become registered on Grabeeter just select "Register" (menu), type in your Twitter name into the intendet textfield and submit.
Your tweets will be added to the searchable pool of tweets on Grabeeter for anybody using Grabeeter.
Only tweets of Twitter users registered on Grabeeter can be searched. In case a Twitter user has his/her account protected no tweets can be searched through. This user is not displayed for the search as long his/her tweets are protected.</p>

