<h3>The Grabeeter Web-interface</h3>
<h4>The Web-interface offers to:</h4>
<ul>
	<li>Register Twitter users to Grabeeter and become their tweets searchable by doing so</li>
	<li>Search tweets of all Twitter users registered on Grabeeter online</li>
	<li>Launch (install) the Grabeeter application for desktop usage</li>
	<li>Use the Grabeeter API for further treatment</li>
</ul>

<p>To search tweets of a Twitter user registered on Grabeeter first select the Twitter user of interest by selecting "Search" (menu). 
All twitter users registered on Grabeeter are displayed. Select the one of interest by clicking on the corresponding icon
Now just type in the search keyword into the intended textfield.
The search will start instantly you typed in at least three characters. Pressing return will start the search as well.
The results are displayed in a list below, listed by time latest above.
If you want to open the original tweet, click on "Acess on Twitter" in the bottom right corner of the tweet.
</p>

<h3>The Grabeeter Desktop Application</h3>
<h4>The application offers you to:</h4>
<ul>
	<li>Search tweets of Twitter users registered on Grabeeter</li>
	<li>Grab your latest tweets in order to be included in the search</li>
	<li>Once you "grabed" your tweets you do not have to be online to search them</li>
	<li>Indicate a time frame for searching by date</li>
</ul>

<h4>Installing and Removing Grabeeter Desktop Application</h4>
<p>
To use the Grabeeter Desktop Application click on the "Launch" button. The application will install and start automatically; it will leave a shortcut on your desktop after you finished.
To remove Grabeeter from your local system again, open your software control panel, look for Grabeeter and click on button "Remove" or "Deinstall"; confirm. 
</p>

<h4>Grab and save your Tweets with Grabeeter Desktop Application</h4>
<p>
Within the application you can search tweets by typing in the username of a Twitter user registered on Grabeeter. 
Confirm with the button "Grab Tweets".
By doing so, tweets of that Twitter user will be downloaded to your system and stored in a <a href="/frequently-asked-questions#tweets-location">location described in the FAQs ("Use the Client Application")</a> section.
</p>

<h4>Search your Tweets with Grabeeter Desktop Application</h4>

<p>
After selecting and grabing tweets of a Twitter user registered on Grabeeter type in your search keyword into the intended textfield and press return or click on button "Search". The results are shown within the textarea below the form.
It is also possible to enter a time period to filter your tweets. Just enter the start of the time peroid into the intended textfield "From" and the end of the peroid into the intended textfield "To". 
The results are displayed in the list below, listed by relevance.
To see details of a result just click on the specific result; the details are displayed below the list of results in an copyable format. To open the original tweet click on "Twitter Link" on the bottom right corner of the application.
Once the tweets are grabed you can use Grabeeter Desktop Application for offline search as well. 
</p>
