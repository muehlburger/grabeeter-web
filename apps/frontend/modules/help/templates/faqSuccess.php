<h2>FAQ</h2>

<h3>What is Grabeeter good for?</h3>
<p>Grabeeter is an application that allows you to search tweets of Twitter users and to save the results locally.</p>

<h3>Which variants can I use of Grabeeter?</h3>
<p>There is the Web-interface and the desktop application
Additionaly the desktop application can be easily put on your desktop by drag&drop.</p>

<h3>How can i search tweets?</h3>
<p>Using the Web-interface just type in the search keyword into the intended textfield "Tweetsearch".</p>

<h3>Which tweets are searched?</h3>
<p>The tweets of the registered Twitter users on Grabeeter can be searched.</p>

<h3>How can I add my tweets to Grabeeter to become them searchable?</h3>
<p>Type your Twitter name into the textfield below "Register" and press return.</p>

<h3>How can i use the desktop variant of Grabeeter?</h3>
<p>Click on the Button "Grabeeter application" or Drag the "Grabeeter on Desktop"-area to your desktop.
The application will install and start automatically and will leave a shortcut on your desktop after you finished.</p>

<h3>What do I need to use Grabeeter Desktop Application locally?</h3>
<p>For Grabeeter runs on Java you need Java being installed on your local system.</p>

<h3>Is there a difference between the desktop variant and the drag&drop variant of Grabeeter?</h3>
<p>No; just feel comfortable about using one of them.</p>

<h3>Can I use Grabeeter offline?</h3>
<p>Yes, use the desktop application and uncheck the "online" select, go on with your search.</p>

<h3>How can I search my tweets?</h3>
<p>Use the desktop application and type in your Twitter name into the intended textfield "Twitter Username", press return or click on button "Retrieve Tweets".
Your tweets are be downloaded locally and displayed in the textarea below the form afterwards.
Type in your search keyword into the intended textfield "Tweets containing" and press return or click on button "Search".
The results are shown within the textarea below the form.</p>

<h3>How can I save my tweets?</h3>
<p>Using the desktop application click on the button "Save tweets locally". Choose path of saving and confirm.
The current tweets displayed within the textarea below the form are saved to a textfile to the path chosen.
</p>

<h3>How can i remove Grabeeter from my desktop?</h3>
<p>Open your software controlpanel, look for Grabeeter and click on button "Remove" or "Deinstall"; confirm.</p>

